/**
 * 當選題 sensitivityLevel==='tender'（死亡／喪親／失能／照護等高敏感門檻）時，
 * 撰寫需額外遵守的溫柔守則。供 write 步驟與 critique 步驟參考。
 */
export const SENSITIVITY_GUIDANCE: string = `
高敏感門檻（tender）鐵律：
  - 溫柔：把每一個習俗都當成「有人正在經歷喪親／照護」來寫，不居高臨下。
  - 具體：描述可觀察的實踐（禮儀、安排、制度），不抽象悲情，也不渲染苦難。
  - 不獵奇：絕不把喪葬或失能習俗當奇風異俗陳列、不以「你絕對想不到」的口吻獵奇。
  - 不消費苦難：苦難不是素材；守望者只見證，不消費。
`.trim();

