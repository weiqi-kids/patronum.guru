// ── 立場事故風險 ──────────────────────────────────────────────────────────────

/**
 * 立場事故風險來自「寫法」，而非門檻本身的敏感度。
 * 一個高敏感的門檻（如送別）若寫法溫柔、歸因處境、不獵奇，風險可以是 low。
 * 一個看似平常的門檻若偷偷裁決、本質化或嘲弄，風險就是 high。
 */
export const STANCE_RISK_CRITERIA: {
  guidance: string;
  highRiskDimensions: string[];
} = {
  guidance: `
立場事故風險評估的是「這篇文章的寫法是否可能被讀者合理詮釋為偷偷裁決、本質化、
嘲弄、或對敏感題失溫獵奇」，不是「這道門檻本身是否敏感」。

low 風險寫法：
  - 呈現光譜，為張力命名，但不裁決哪一側比較對。
  - 把態度差異歸因於「處境」（房價、長照制度、人口結構、宗教傳統、歷史），
    而非「民族天生如何」。
  - 對每個文化都用同等的理解之同情書寫；敏感題溫柔、具體、不獵奇。

high 風險寫法（應觸發 critique 重寫或拒絕）：
  - 偷偷裁決：表面中立，實則暗示某文化「比較孝順／進步／正確」。
  - 本質化（essentializing）：把某族群的行為說成「天生」「民族基因」「文化 DNA」。
  - 嘲弄（mocking）：用帶嘲諷意味的形容詞或比喻描述某文化的家庭習俗。
  - 敏感題失溫獵奇（sensationalizing）：把死亡／喪親／失能／照護當奇觀陳列、消費苦難。
  - 與高敏維度（見 highRiskDimensions）交叉時，需特別謹慎。
`.trim(),
  highRiskDimensions: [
    '死亡與喪俗（喪葬儀式、守靈、服喪——避免獵奇與消費苦難）',
    '失能與照護（失智、長期臥床、家庭照護者的負荷——具體而不悲情消費）',
    '性別與世代角色（誰理所當然該照顧／持家／養老——避免本質化性別分工）',
    '宗教與喪葬（將喪俗與宗教信仰直接因果連結而忽略處境與歷史）',
    '孝道道德化（把「孝順」當成可排名的道德分數，暗中裁決哪個文化更孝）',
  ],
};

